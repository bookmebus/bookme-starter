module Enumable
  class EventState
    
    #Not specified.
    EVENT_STATE_UNSPECIFIED = 0
    
    #The event is scheduled.
    SCHEDULED = 1
    
    #The event is rescheduled.
    RESCHEDULED = 2
    
    #The event is cancelled.
    CANCELLED = 3
    
    #The event is postponed.
    POSTPONED = 4
  end

end